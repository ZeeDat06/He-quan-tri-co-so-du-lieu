CREATE FUNCTION TIMDOANTHEOGIA
(
    @GiaMin DECIMAL(10, 2),
    @GiaMax DECIMAL(10, 2)
)
RETURNS TABLE
AS
RETURN
(
    SELECT *
    FROM DOAN
    WHERE Gia BETWEEN @GiaMin AND @GiaMax
);
GO

CREATE FUNCTION TIMDOUONGTHEOGIA
(
    @GiaMin DECIMAL(10, 2),
    @GiaMax DECIMAL(10, 2)
)
RETURNS TABLE
AS
RETURN
(
