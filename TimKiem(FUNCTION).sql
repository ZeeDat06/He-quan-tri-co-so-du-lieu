GO

CREATE FUNCTION TIMKHACHHANGTHEOSDT
(
    @SDT NVARCHAR(15)
)
RETURNS TABLE
AS
RETURN
(
    SELECT *
    FROM KHACHHANG
    WHERE SDT = @SDT
);
GO

CREATE FUNCTION TIMHOADONTHEONGAY
(
    @NgayBatDau DATETIME,
    @NgayKetThuc DATETIME
)
RETURNS TABLE
AS
RETURN
(
    SELECT *
    FROM HOADON
    WHERE NgayLap BETWEEN @NgayBatDau AND @NgayKetThuc
);
GO

